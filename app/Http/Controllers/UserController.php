    public function action($action, $id)
    {
        if ($action) {
            $user = User::find($id);
            switch ($action) {
                case 'active':
                    $user->active = $user->active ? 0 : 1;
                    break;
                case 'hot':
                    $user->hot = $user->hot ? 0 : 1;
                    break;
               
            }
            $user->save();
        }
        return redirect()->back();
    }

